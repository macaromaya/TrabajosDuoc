    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed

        if (!controlador.registro.existePeli(Integer.parseInt(codigo.getText()))) {
            
            String k="";
            if (uhd.isSelected()){ k="s";} else {k="n";}
           
            pelicula p = new pelicula(
                    Integer.parseInt(codigo.getText()),
                    Integer.parseInt(precio.getText()),
                    Integer.parseInt(categoria.getSelectedItem().toString().substring(0,categoria.getSelectedItem().toString().indexOf("."))),
                    k,//formato
                    nombre.getText()
            );

            if (Integer.parseInt(codigo.getText()) >= 10000 && Integer.parseInt(codigo.getText()) <= 99999 && nombre.getText().length()>=3 && Integer.parseInt(precio.getText()) > 1000 ) {
                if (controlador.registro.grabarPeli(p)) {
                    javax.swing.JOptionPane.showMessageDialog(this, "PELICULA agregada");

                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "no se pudo guardar");
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "No cumple las reglas de negocio \n "
                        + "- El Codigo debe ser un numero entre 10000 y 99999  \n"
                        + "- El nombre de la película debe tener 3 caracteres como mínimo \n"
                        + "- El precio de la película debe ser mayor a 1000 ");
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "codigo existe");
        }
    }//GEN-LAST:event_guardarActionPerformed

